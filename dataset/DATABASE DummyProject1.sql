CREATE TABLE movies (
	movie_id INT PRIMARY KEY,
    title VARCHAR(255),
    release_year INT,
    genre VARCHAR(50),
    budget DECIMAL(15,2),
    director VARCHAR(100),
    is_3d BOOLEAN
) ENGINE = InnoDB;

CREATE TABLE box_office (
	box_office_id INT PRIMARY KEY,
    movie_id INT,
    country VARCHAR(50),
    revenue DECIMAL(15,2),
    FOREIGN KEY (movie_id) REFERENCES movies(movie_id)
) ENGINE = InnoDB;

